package ianculescu.alexandra.g1098.ex1.chain;

public class Sensor {

	private int priority;
	private String text;
	public Sensor(int priority, String text) {
		super();
		this.priority = priority;
		this.text = text;
	}
	public int getPriority() {
		return priority;
	}
	public String getText() {
		return text;
	}
	
	
}
